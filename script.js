// Get HTML elements
let promptdiv = document.getElementById("promptdiv");
let typeinput = document.getElementById("typeinput");
let timeoutput = document.getElementById("timeoutput");
let resetbtn = document.getElementById("resetbtn");
let wpmoutput = document.getElementById("wpmoutput");

// Set defaults
let line;
let numwords = 10;

function createword() {
  promptdiv.replaceChildren();

  line = "";

  if (isNaN(numwords)) {
    line =
      "I can't do api requests using online editor haha. I spent 30 minutes trying to figure out what the fuck was wrong for no reason lfg.";
  } else {
    for (let i = 0; i < numwords; i++) {
      line += wordbank[Math.floor(Math.random() * wordbank.length)] + " ";
    }

    line = line.slice(0, -1);
  }

  for (const c of line.split("")) {
    let span = document.createElement("span");
    span.innerHTML = c;
    promptdiv.appendChild(span);
  }
}

// Create default prompt
createword();

// Text highlighting
let wordcountbtns = document.getElementsByClassName("wordcountbtn");

for (const btn of wordcountbtns) {
  btn.addEventListener("click", function (event) {
    if (isNaN(btn.innerHTML)) {
      numwords = "quote";
    } else {
      numwords = btn.innerHTML;
    }

    for (const b of wordcountbtns) {
      b.style.color = "rgb(211, 211, 211)";
    }

    btn.style.color = "rgb(109, 209, 139)";

    reset();
  });
}

let started = false;
let startTime;
let start;
let totaltime;

typeinput.addEventListener("input", function (event) {
  let typeinputtext = typeinput.value;
  let splittext = typeinputtext.split("");
  let spans = promptdiv.children;

  if (!started) {
    started = true;
    startTime = Date.now();
    start = setInterval(starttimeoutput, 10);
  }

  let allcorrect = true;

  for (let x = 0; x < spans.length; x++) {
    if (splittext[x] == null) {
      spans[x].style.color = "lightslategray";
      spans[x].style.textDecoration = "none";
      allcorrect = false;
    } else if (splittext[x] !== spans[x].innerHTML) {
      spans[x].style.color = "red";
      spans[x].style.textDecoration = "underline";
      allcorrect = false;
    } else {
      spans[x].style.color = "white";
      spans[x].style.textDecoration = "none";
    }
  }

  if (allcorrect) {
    started = false;
    clearInterval(start);
    wpmoutput.innerHTML = Math.round((line.length / 5 / totaltime) * 60) + " wpm";
    createword();
    typeinput.value = "";
  }

  if (typeinputtext == "tappytype") {
    document.getElementById("title").innerHTML = "derekshatypes150wpm";
  }
});

function starttimeoutput() {
  totaltime = Math.round(100 * ((Date.now() - startTime) / 1000)) / 100;
  wpmoutput.innerHTML = Math.round((typeinput.value.length / 5 / totaltime) * 60) + " wpm";
  timeoutput.innerHTML = totaltime + " sec";
}

resetbtn.addEventListener("click", reset);

document.addEventListener("keyup", function (event) {
  if (event.key == "Escape" || event.key == "Tab") {
    reset();
    typeinput.focus();
  }
});

function reset() {
  typeinput.value = "";
  started = false;
  clearInterval(start);
  wpmoutput.innerHTML = "0 wpm";
  timeoutput.innerHTML = "0.00" + " sec";
  for (const y of promptdiv.children) {
    y.style.color = "lightslategray";
    y.style.textDecoration = "none";
  }
  createword();
  typeinput.focus();
}
